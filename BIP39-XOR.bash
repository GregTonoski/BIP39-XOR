fn_bip39_checksum () {
  declare sha256_digest_hex=""
  declare -i bip39_checksum=0
  declare -i counter=0
  
  if command -v sha256sum >/dev/null ; then
    sha256_digest_hex=$( for (( counter=0; counter<${#1}; counter+=2 )); do printf "\x${1:$counter:2}"; done | sha256sum -b )
  elif command -v shasum >/dev/null ; then
    sha256_digest_hex=$( for (( counter=0; counter<${#1}; counter+=2 )); do printf "\x${1:$counter:2}"; done | shasum -a 256 -b )
  elif command -v openssl >/dev/null ; then
    sha256_digest_hex=$( for (( counter=0; counter<${#1}; counter+=2 )); do printf "\x${1:$counter:2}"; done | openssl dgst -digest -sha256 -r )
  else
    echo "There isn't sha256sum or shasum or openssl installed. Script execution interrupted."
    exit 6
  fi

  printf -v "bip39_checksum" "%d" $(( 0x${sha256_digest_hex: 0: ( ${BIP39_CHECKSUM_BITS_COUNT} + 3 ) / ${BITS_IN_NIBBLE} } ))
  if (( ${BIP39_CHECKSUM_BITS_COUNT} % ${BITS_IN_NIBBLE} != 0 )) ; then
    bip39_checksum=$(( ${bip39_checksum} / (2** ( ${BITS_IN_NIBBLE} - ( ${BIP39_CHECKSUM_BITS_COUNT} % ${BITS_IN_NIBBLE} ) ) ) ))
  fi
  FN_BIP39_CHECKSUM_RESULT=${bip39_checksum}
}

fn_bitwiseXOR () {
  declare -i position=0
  RESULT_XOR=""
  for (( position=0; position<${#1}; position+=1 )); do
    RESULT_XOR="${RESULT_XOR}$( printf "%01X" $(( 16#${1:$position:1} ^ 16#${2:$position:1} )) )"
  done
}

fn_pick_at_random () {

  declare -i i=0
  CANDIDATE_KEY=""

  if ! [[ -z ${SRANDOM+foobar} ]] ; then
    for (( i=0 ; i < ${1} ; i=$(( ${i} + ( ${BITS_IN_SRANDOM}/${BITS_IN_NIBBLE} ) )) )) ; do
      printf -v "CANDIDATE_KEY" "%s%08X" "${CANDIDATE_KEY}" "$SRANDOM"
    done
  elif ! [[ -z ${RANDOM+foobar} ]]; then   # if $SRANDOM is not supported like in older version (e.g. 3.2) of Bash and zsh then use $RANDOM
    for (( i=0 ; i < ${1} ; i=$(( ${i} + ( ${BITS_IN_RANDOM}/${BITS_IN_NIBBLE} ) )) )) ; do
      printf -v "CANDIDATE_KEY" "%s%04X" "${CANDIDATE_KEY}" "$RANDOM"
    done
  else
    echo "ERROR: can't source random numbers on this computer."
    exit 5
  fi
  printf -v "CANDIDATE_KEY" "%.*s" "${1}" "${CANDIDATE_KEY}"
}

