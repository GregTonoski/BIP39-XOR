elif (( $# == 13 || $# == 16 || $# == 19 || $# == 22 )) ; then
  if [[ "${!#}" = "XOR" ]]; then
    for (( i=1 ; i <  $#  ; i++ )) ; do
      INPUT_CODEWORDS_ARRAY1="${INPUT_CODEWORDS_ARRAY1}${!i} "
    done
  fi
elif (( $# == 25 || $# == 31 || $# == 37 || $# == 43 || $# == 49 )) ; then
  median=$(( $#/2 + 1 ))
  if [[ "${!median}" = "XOR" ]] ; then
    for (( i=1 ; i <=  $# / 2 ; i++ )) ; do
      INPUT_CODEWORDS_ARRAY1="${INPUT_CODEWORDS_ARRAY1}${!i} "
    done
    for (( i=$(($# / 2 + 2)) ; i <= $# ; i++ )); do
    INPUT_CODEWORDS_ARRAY2="${INPUT_CODEWORDS_ARRAY2}${!i} "
    done
  elif [[ "${25}" = "XOR" ]]; then
    for (( i=1 ; i <  25 ; i++ )) ; do
      INPUT_CODEWORDS_ARRAY1="${INPUT_CODEWORDS_ARRAY1}${!i} "
    done
  else
    echo "ERROR: the input words are not accepted because XOR keyword/delimiter is missing or misplaced."
    exit 2
  fi
elif  [[ "${1}" = "-h" || "${1}" = "--help" || $# -eq 0 ]] ; then
  helptext_show
  exit 2
elif [[ "${1}" = "--auto-input" ]] ; then
  fn_pick_at_random 32
  ENTROPY_HEX=${CANDIDATE_KEY}
  BIP39_CHECKSUM_BITS_COUNT=$(( ${#ENTROPY_HEX} * ${BITS_IN_NIBBLE} / 33 ))
  fn_bip39_checksum ${ENTROPY_HEX}
  ENTROPY_CHECKSUM_BIP39=${FN_BIP39_CHECKSUM_RESULT}
else
  echo "ERROR: the input words are not accepted. Either there are too many or too few of them or the \"XOR\" delimiting keyword is missing or misplaced. There were $# words read."
  helptext_show
  exit 1
fi

if [[ -n "${INPUT_CODEWORDS_ARRAY1}" ]] ; then
  fn_eng_words_to_hex ${INPUT_CODEWORDS_ARRAY1}
  INPUT_CODEWORDS_ARRAY1_AS_HEX=${FN_ENG_WORDS_TO_HEX_RESULT}
  BIP39_CHECKSUM_BITS_COUNT=$(( ${#INPUT_CODEWORDS_ARRAY1_AS_HEX} * ${BITS_IN_NIBBLE} / 33 ))
  ENTROPY_CHECKSUM_BIP39=$(( 16#${INPUT_CODEWORDS_ARRAY1_AS_HEX: ${#INPUT_CODEWORDS_ARRAY1_AS_HEX} - ( ${BIP39_CHECKSUM_BITS_COUNT} + 3 ) / ${BITS_IN_NIBBLE} } ))
  bits_to_shift=$(( ${BIP39_CHECKSUM_BITS_COUNT} % ${BITS_IN_NIBBLE} ))
  if (( ${bits_to_shift} != 0 )) ; then
    ENTROPY_CHECKSUM_BIP39=$(( ${ENTROPY_CHECKSUM_BIP39} % ( 2 ** ${BIP39_CHECKSUM_BITS_COUNT} ) ))
    i=$(( ${#INPUT_CODEWORDS_ARRAY1_AS_HEX} - ( ${BIP39_CHECKSUM_BITS_COUNT} + 3 ) / ${BITS_IN_NIBBLE} ))
    last_bits_value=$(( 0x${INPUT_CODEWORDS_ARRAY1_AS_HEX: ${i}: 1} / 2** ${bits_to_shift} ))
    first_bits_value=$(( ( 0x${INPUT_CODEWORDS_ARRAY1_AS_HEX: ${i} - 1: 1} % 2 ** ${bits_to_shift} ) * 2** ( ${BITS_IN_NIBBLE} - ${bits_to_shift} ) ))
    printf -v "NEW_NIBBLE" "%01X" $(( ${last_bits_value} + ${first_bits_value} ))
    printf -v "ENTROPY_HEX" "%c%s" "${NEW_NIBBLE}" "${ENTROPY_HEX}"
    for (( i=$(( ${#INPUT_CODEWORDS_ARRAY1_AS_HEX} - ${#ENTROPY_CHECKSUM_BIP39} - 1 )) ; i > 0 ; i=i-1 )) ; do
      last_bits_value=$(( 0x${INPUT_CODEWORDS_ARRAY1_AS_HEX: ${i}: 1} / 2** ${bits_to_shift}  ))
      first_bits_value=$(( ( 0x${INPUT_CODEWORDS_ARRAY1_AS_HEX: ${i} - 1: 1} % 2 **( ${bits_to_shift} ) ) * 2** ( ${BITS_IN_NIBBLE} - ${bits_to_shift} ) ))
      printf -v "NEW_NIBBLE" "%01X" $(( ${last_bits_value} + ${first_bits_value} ))
      printf -v "ENTROPY_HEX" "%c%s" "${NEW_NIBBLE}" "${ENTROPY_HEX}"
    done
  else
    ENTROPY_HEX=${INPUT_CODEWORDS_ARRAY1_AS_HEX: 0: ${#INPUT_CODEWORDS_ARRAY1_AS_HEX} - ( ${BIP39_CHECKSUM_BITS_COUNT} + 3 ) / ${BITS_IN_NIBBLE} }
  fi
fi

# Explanation of the corner case: it is impossible to generate a matching key if an input key value is 0
declare -i sum=0
for (( i=0 ; i<${#ENTROPY_HEX} ; i+=1 )) ; do
  sum=$(( ${sum} + 0x${ENTROPY_HEX: i: 1} ))
done
if [[ "${sum}" -eq 0 && 0x${ENTROPY_CHECKSUM_BIP39} -ne 0 ]] ; then
  echo "There isn't any entropy value that would satisfy the condition of result of XOR calculation of BIP39 checksums being equal BIP39 checksum of input."
  exit 4
fi

if [[ -z "${INPUT_CODEWORDS_ARRAY2}" ]] ; then
  fn_pick_at_random ${#ENTROPY_HEX}
  ENCRYPTION_KEY1=${CANDIDATE_KEY}
  fn_bip39_checksum ${ENCRYPTION_KEY1}
  ENCRYPTION_KEY1_CHECKSUM_BIP39=${FN_BIP39_CHECKSUM_RESULT}
  fn_bitwiseXOR ${ENCRYPTION_KEY1} ${ENTROPY_HEX}
  ENCRYPTION_KEY2=${RESULT_XOR}
  fn_bip39_checksum ${ENCRYPTION_KEY2}
  ENCRYPTION_KEY2_CHECKSUM_BIP39=${FN_BIP39_CHECKSUM_RESULT}
  while (( ( ${ENTROPY_CHECKSUM_BIP39} ^ ${ENCRYPTION_KEY1_CHECKSUM_BIP39}) != ${ENCRYPTION_KEY2_CHECKSUM_BIP39} )); do
    fn_pick_at_random ${#ENTROPY_HEX}
    ENCRYPTION_KEY1=${CANDIDATE_KEY}
    fn_bip39_checksum ${ENCRYPTION_KEY1}
    ENCRYPTION_KEY1_CHECKSUM_BIP39=${FN_BIP39_CHECKSUM_RESULT}
    printf "Candidate encryption key with checksum BIP-39: 0x%s 0x%02X\r" "${ENCRYPTION_KEY1}" ${ENCRYPTION_KEY1_CHECKSUM_BIP39}
    fn_bitwiseXOR ${ENCRYPTION_KEY1} ${ENTROPY_HEX}
    ENCRYPTION_KEY2=${RESULT_XOR}
    fn_bip39_checksum ${ENCRYPTION_KEY2}
    ENCRYPTION_KEY2_CHECKSUM_BIP39=${FN_BIP39_CHECKSUM_RESULT}
  done
  printf "                                                                                              \r"
  fn_hex_to_bip39_eng_words "${ENCRYPTION_KEY1}" ${ENCRYPTION_KEY1_CHECKSUM_BIP39} ${BIP39_CHECKSUM_BITS_COUNT}
  printf "%s \n" "${BIP39_WORDS}"
  fn_hex_to_bip39_eng_words "${ENCRYPTION_KEY2}" ${ENCRYPTION_KEY2_CHECKSUM_BIP39} ${BIP39_CHECKSUM_BITS_COUNT}
  echo "XOR"
  printf "%s \n" "${BIP39_WORDS}"
else
  fn_eng_words_to_hex ${INPUT_CODEWORDS_ARRAY2}
  INPUT_CODEWORDS_ARRAY2_AS_HEX=${FN_ENG_WORDS_TO_HEX_RESULT}
  fn_bitwiseXOR "${INPUT_CODEWORDS_ARRAY1_AS_HEX}" "${INPUT_CODEWORDS_ARRAY2_AS_HEX}"
  fn_hex_to_bip39_eng_words ${RESULT_XOR} 0 0
  echo "${INPUT_CODEWORDS_ARRAY2}"
  echo "XOR"
  printf "%s \n" "${BIP39_WORDS}"
fi
