  SHA2_WORDS6=$(( ( ${SHA2_WORDS6} + ${g} ) % 4294967296 ))
  
  SHA2_WORDS7=$(( ( ${SHA2_WORDS7} + ${h} ) % 4294967296 ))

  FN_SHA256_RESULT=""
  i=0
  while [ ${i} -lt 8 ] ; do
    eval "sha256_word=\$SHA2_WORDS${i}"
    FN_SHA256_RESULT=$( printf "%s%08X" "${FN_SHA256_RESULT}" "${sha256_word}" )
    i=$(( ${i} + 1 ));
  done
}

fn_bip39_checksum () {
  sha256_digest_hex=""
  bip39_checksum=0
  counter=0
  hex_substring=${1}
  hex_suffix=""
  
  if [ ! -z "${BASH_VERSION+foobar}" -o ! -z "${ZSH_VERSION+foobar}" ] ; then
    if command -v sha256sum >/dev/null ; then
      sha256_digest_hex=$( while [ ${hex_substring} ] ; do printf "\x${hex_substring%${hex_substring#??}}" ; hex_substring=${hex_substring#??} ; done | sha256sum -b )
    elif command -v shasum >/dev/null ; then
      sha256_digest_hex=$( while [ ${hex_substring} ] ; do printf "\x${hex_substring%${hex_substring#??}}" ; hex_substring=${hex_substring#??} ; done | shasum -a 256 -b )
    elif command -v openssl >/dev/null ; then
      sha256_digest_hex=$( while [ ${hex_substring} ] ; do printf "\x${hex_substring%${hex_substring#??}}" ; hex_substring=${hex_substring#??} ; done | openssl dgst -digest -sha256 -r )
    else
      fn_sha256 ${1}
      sha256_digest_hex=${FN_SHA256_RESULT}
    fi
  else
    fn_sha256 ${1}
    sha256_digest_hex=${FN_SHA256_RESULT}
  fi
  counter=$(( ( ${BIP39_CHECKSUM_BITS_COUNT} + 3 ) / ${BITS_IN_NIBBLE} ))
  hex_suffix=${sha256_digest_hex}
  while [ ${counter} -gt 0 ] ; do
    hex_suffix=${hex_suffix#?}
    counter=$(( ${counter} - 1 ))
  done
  bip39_checksum=$( printf "%d" "0x${sha256_digest_hex%%${hex_suffix}}" )
  if [ $(( ${BIP39_CHECKSUM_BITS_COUNT} % ${BITS_IN_NIBBLE} )) -ne 0 ] ; then
    fn_power $(( ( ${BITS_IN_NIBBLE} - ( ${BIP39_CHECKSUM_BITS_COUNT} % ${BITS_IN_NIBBLE} ) ) ))
    bip39_checksum=$(( ${bip39_checksum} / ${FN_POWER_RESULT} ))
  fi
  FN_BIP39_CHECKSUM_RESULT="${bip39_checksum}"
}

fn_bitwiseXOR () {
  substring_a=${1}
  substring_b=${2}

  while [ ${#substring_a} -lt ${#substring_b} ] ; do
    substring_a="0${substring_a}"
  done
  while [ ${#substring_b} -lt ${#substring_a} ] ; do
    substring_b="0${substring_b}"
  done
  while [ ${#substring_a} -gt 0 ] ; do
    printf "%01X" $(( 0x${substring_a%%${substring_a#?}} ^ 0x${substring_b%%${substring_b#?}} ))
    substring_a="${substring_a#?}"
    substring_b="${substring_b#?}"
  done
}

fn_pick_at_random () {

  i=0
  CANDIDATE_KEY=""
  read_random=""

  if [ ! -z ${SRANDOM+foobar} ] ; then
    while [ ${i} -lt ${1} ] ; do
      CANDIDATE_KEY=$( printf "%s%08X" "${CANDIDATE_KEY}" "$SRANDOM" )
      i=$(( ${i} + ( ${BITS_IN_SRANDOM}/${BITS_IN_NIBBLE} ) ))
    done
  elif [ ! -z ${RANDOM+foobar} ] ; then   # if $SRANDOM is not supported like in older version (e.g. 3.2) of Bash and zsh then use $RANDOM
    while [ ${i} -lt ${1} ] ; do
      CANDIDATE_KEY=$( printf "%s%04X" "${CANDIDATE_KEY}" "$RANDOM" )
      i=$(( ${i} + ( ${BITS_IN_RANDOM}/${BITS_IN_NIBBLE} ) ))
    done
  elif [ -e '/dev/random' ] ; then
    IFS=
    while [ "${1}" -gt "${#CANDIDATE_KEY}" ] ; do
      if read read_random < '/dev/random' ; then
        CANDIDATE_KEY=$( while [ ${#read_random} -gt 0 ] ; do printf "%02X" "'${read_random%%${read_random#?}}" ; read_random="${read_random#?}" ; done ; )
        CANDIDATE_KEY="${CANDIDATE_KEY}0A"
     else
       CANDIDATE_KEY="${CANDIDATE_KEY}00"
     fi
    done
    unset ${IFS}
  fi
  CANDIDATE_KEY=$( printf "%.*s" "${1}" "${CANDIDATE_KEY}" )
  CANDIDATE_KEY=${CANDIDATE_KEY%00000000000000000000000000000000*} # sanity check that entropy isn't none and a key is not trivial
  if [ ${#CANDIDATE_KEY} -lt ${1} ] ; then
    printf "Input a candidate key as hexadecimal numeral (must be max %d characters long): " "${1}"
    read CANDIDATE_KEY
  fi
  if [ -z ${#CANDIDATE_KEY} ] ; then
    printf "Error: candidate key is empty.\n"
    exit 7
  fi
}

fn_parse_arguments () {
  argument=""
  eval "set -- $@" # in order to process quoted arguments (user input) as well
  if [ $# -eq 30 -o $# -eq 36 -o $# -eq 42 -o $# -eq 48 ] ; then
    i=1
    while [ ${i} -le $(( $# / 2 )) ] ; do
      eval "argument=\${${i}}"
      INPUT_CODEWORDS_ARRAY1="${INPUT_CODEWORDS_ARRAY1}${argument} "
      i=$(( ${i} + 1 ))
    done
    i=$(($# / 2 + 1))
    while [ ${i} -le $# ] ; do
      eval "argument=\${${i}}"
      INPUT_CODEWORDS_ARRAY2="${INPUT_CODEWORDS_ARRAY2}${argument} "
      i=$(( ${i} + 1 ))
    done
  elif [ $# -eq 12 -o $# -eq 15 -o $# -eq 18 -o $# -eq 21 -o $# -eq 24 ] ; then
    INPUT_CODEWORDS_ARRAY1="${@}"
  elif [ $# -eq 13 -o $# -eq 16 -o $# -eq 19 -o $# -eq 22 ] ; then
    eval "argument=\${${#}}"
    if [ "${argument}" = "XOR" ] ; then
      i=1
      while [ ${i} -lt  $# ] ; do
        eval "argument=\${${i}}"
        INPUT_CODEWORDS_ARRAY1="${INPUT_CODEWORDS_ARRAY1}${argument} "
        i=$(( ${i} + 1 ))
     done
    fi
  elif [ $# -eq 25 -o $# -eq 31 -o $# -eq 37 -o $# -eq 43 -o $# -eq 49 ] ; then
    median=$(( $#/2 + 1 ))
    eval "argument=\${${median}}"
    if [ "${argument}" = "XOR" ] ; then
      i=1
      while [ ${i} -le $(( $# / 2 )) ] ; do
        eval "argument=\${${i}}"
        INPUT_CODEWORDS_ARRAY1="${INPUT_CODEWORDS_ARRAY1}${argument} "
        i=$(( ${i} + 1 ))
      done
      i=$(($# / 2 + 2))
      while [ ${i} -le $# ] ; do
        eval "argument=\${${i}}"
        INPUT_CODEWORDS_ARRAY2="${INPUT_CODEWORDS_ARRAY2}${argument} "
        i=$(( ${i} + 1 ))
      done
    elif [ "${25}" = "XOR" ] ; then
      i=1
      while [ "${i}" -lt  25 ] ; do
        eval "argument=\${${i}}"
        INPUT_CODEWORDS_ARRAY1="${INPUT_CODEWORDS_ARRAY1}${argument} "
         i=$(( ${i} + 1 ))
      done
    else
      echo "ERROR: the input words are not accepted because XOR keyword/delimiter is missing or misplaced."
      exit 2
    fi
  elif  [ "${1}" = "-h" -o "${1}" = "--help" ] ; then
    helptext_show
    exit 2
  elif [ "${1}" = "--auto-input" ] ; then
    fn_pick_at_random 32
    ENTROPY_HEX=${CANDIDATE_KEY}
    BIP39_CHECKSUM_BITS_COUNT=$(( ${#ENTROPY_HEX} * ${BITS_IN_NIBBLE} / 32 ))
    fn_bip39_checksum ${ENTROPY_HEX}
    ENTROPY_CHECKSUM_BIP39=${FN_BIP39_CHECKSUM_RESULT}
  else
    echo "ERROR: the input words are not accepted. Either there are too many or too few of them or the \"XOR\" delimiting keyword is missing or misplaced. There were $# words read."
    helptext_show
    exit 1
  fi
}

fn_separate_entropy_and_checksum_BIP39_hex () {
  first_bits_value=0
  last_bits_value=0
  ENTROPY_HEX=""
  bits_to_shift=0
  NEW_NIBBLE=""
  
  
  i=$(( ${#1} - ${2} / ${BITS_IN_NIBBLE} ))
  hex_prefix=${1}
  while [ ${i} -lt ${#1} ] ; do
    hex_prefix=${hex_prefix%?}
    i=$(( ${i} + 1 ))
  done
  ENTROPY_CHECKSUM_BIP39=$(( 0x${1#${hex_prefix}} ))
  bits_to_shift=$(( ${2} % ${BITS_IN_NIBBLE} ))
  if [ "${bits_to_shift}" -gt 0 ] ; then
    fn_power ${2}
    ENTROPY_CHECKSUM_BIP39=$(( 0x${1#${hex_prefix%?}} % ${FN_POWER_RESULT} ))
    while [ ${#hex_prefix} -gt 1 ] ; do
      fn_power ${bits_to_shift}
      last_bits_value=$(( 0x${hex_prefix#${hex_prefix%?}} / ${FN_POWER_RESULT} ))
      hex_prefix=${hex_prefix%?}
      first_bits_value=$(( ( 0x${hex_prefix#${hex_prefix%?}} % ${FN_POWER_RESULT} ) ))
      fn_power $(( ${BITS_IN_NIBBLE} - ${bits_to_shift} ))
      first_bits_value=$(( ${first_bits_value} * ${FN_POWER_RESULT} ))
      NEW_NIBBLE=$( printf "%01X" $(( ${last_bits_value} + ${first_bits_value} )) )
      ENTROPY_HEX=$( printf "%c%s" "${NEW_NIBBLE}" "${ENTROPY_HEX}" )
    done
  else
    ENTROPY_HEX=${hex_prefix}
  fi
}
